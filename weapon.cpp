#include "weapon.h"
#include <iostream>
#include <stdio.h>
#include <cstdio>
#include <string>
#include <windows.h>

using namespace std;

Weapon Weapon::initializeWeapon(string name, int damage, int type)
{
	Weapon weapon;
	weapon.name = name;
	weapon.damage = damage;
	weapon.type = type;
	return weapon;

}
Weapon Weapon::inputWeapon()
{
	Weapon weapon = {};
	cout << "Выберите название оружия: " << endl;
	cin >> weapon.name;
	cout << "Введите урон оружия: " << endl;
	cin >> weapon.damage;
	do
	{
		cout << "Введите тип меча(номер ответа)\n1) Меч\n2) Топор\n3) Копьё" << endl;
		cin >> weapon.type;
	} while (weapon.type < 1 || weapon.type>3);
	return weapon;
}
void Weapon::outputWeapon(Weapon weapon)
{
	cout << "Название: ";
	cout << weapon.name << endl;
	cout << "Урон: ";
	cout << weapon.damage << endl;
	switch (weapon.type)
	{
	case 1:
	{
		cout << "Тип: Меч\n";
		break;
	}
	case 2:
	{
		cout << "Тип: Топор\n";
		break;
	}
	case 3:
		cout << "Тип: Копьё\n";
		break;
	}

}
int Weapon::getDamage(Weapon weapon)
{
	int damage = weapon.damage;
	return damage;
}

